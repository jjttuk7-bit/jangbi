function pickFields(diagnosis: DiagnosisData, ids: number[]): string {
  return DIAGNOSIS_ITEMS.filter((i) => ids.includes(i.id))
    .map((i) => {
      const v = diagnosis[i.id];
      const value = v != null && String(v).trim() !== "" ? String(v) : "데이터 없음";
      return `- ${i.label}: ${value}`;
    })
    .join("\n");
}

