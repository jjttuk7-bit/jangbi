function filledFields(diagnosis: DiagnosisData): string {
  return DIAGNOSIS_ITEMS.filter((i) => {
    const v = diagnosis[i.id];
    return v != null && String(v).trim() !== "";
  })
    .map((i) => `- ${i.label}: ${diagnosis[i.id]}`)
    .join("\n");
}

